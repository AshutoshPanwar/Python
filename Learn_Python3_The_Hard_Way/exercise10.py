# Different escape Sequence

taddy_cat = "\tI'm tabbed in."      # (\t) refers to tab
persian_cat = "I'm split\non a line."       # (\n) refers to next line
backslash_cat = "I'm \\ a \\ cat."      # (\\) refers to back slash

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(taddy_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
