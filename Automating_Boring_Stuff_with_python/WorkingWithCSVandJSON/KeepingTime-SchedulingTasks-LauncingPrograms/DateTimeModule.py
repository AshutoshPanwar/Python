import datetime

# Know current date-Time
print(datetime.datetime.now())

# Accessing specific value of datatime module
dt = datetime.datetime.now()
print(dt.year)      # Know year
print(dt.month)     # know month
print(dt.day)       # know day
print(dt.hour)      # know hour
print(dt.minute)    # know minutes
print(dt.second)    # know seconds