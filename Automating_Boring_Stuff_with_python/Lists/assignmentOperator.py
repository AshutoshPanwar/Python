spam = 2
# Naive method
spam = spam + 1
print(spam)


spam = 2
# Python style method
spam += 1
print(spam)


# Some common operators
spam = 2
spam -= 1       # spam = spam - 1
print(spam)

spam = 2
spam *= 1       # spam = spam * 1
print(spam)

spam = 2
spam /= 1       # spam = spam / 1
print(spam)

spam = 2
spam %= 1       # spam = spam % 1
print(spam)
