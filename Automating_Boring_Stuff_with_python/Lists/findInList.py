# .index() will return index of item in the list

spam = ['hello', 'hi', 'howdy', 'hayes']

print(spam)
print("> Index of 'hi' in list is:")
print(spam.index('hi'))     # .index() return the index of element is the list

print("> Index of 'hayes' in list is:")
print(spam.index('hayes'))     # .index() return the index of element is the list