# Appending elements to the end of list.
motorcycles = ['honda', 'Royal Enfield', 'BMW']
print(motorcycles)

motorcycles.append('Ducati')
print(motorcycles)


# Inserting element into a specific positon of a list.
motorcycles = ['honda', 'Royal Enfield', 'BMW']
motorcycles.insert(0, 'KTM')
print(motorcycles)